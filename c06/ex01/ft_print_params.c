#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	x;

	i = 1;
	while (i < argc)
	{
		x = 0;
		while (argv[i][x])
		{
			write(1, &argv[i][x], 1);
			x += 1;
		}
		i += 1;
		write(1, "\n", 1);
	}
}
