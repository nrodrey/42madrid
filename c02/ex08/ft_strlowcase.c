#include <stdlib.h>

char	*ft_strlowcase(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += ('a' - 'A');
		i += 1;
	}
	return (str);
}
