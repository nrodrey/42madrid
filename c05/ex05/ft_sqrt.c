#include <stdio.h>
#include <unistd.h>

int	ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);
	i = 0;
	while (i * i <= nb && i <= 46340)
		if (i * i == nb)
			return (i);
		else
			i += 1;
	return (0);
}
